import Foundation

// 상수 -> 값 변경 불가
let a: Int = 100

// 변수 -> 값 변경 가능
var b: Int = 200
b = 300

// Int
var someInt: Int = -100

// UInt -> 부호가 없는 64bit 정수만 가짐
var someUInt: UInt = 200

// Float
var someFloat: Float = 1.1
someFloat = 1
print(someFloat)

// Double
var someDouble: Double = 1.1
someDouble = 1

// Bool
var someBool: Bool = true
someBool = false

// Character
var someCharacter: Character = "가"
someCharacter = "A"
someCharacter = "X"

// String
var someString: String = "안녕하세요"

// 타입추론
var number = 10;

